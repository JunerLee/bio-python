# ============================================================
# 第一部分：print() —— 在屏幕上显示内容
# ============================================================

# print() 是最基础的函数，用来把内容"打印"到屏幕上
# 就像在实验记录本上写下观察结果

print("Hello, Bioinformatics!")         # 打印一句问候
print("欢迎来到生物信息学 Python 课程！")  # 也可以打印中文

# 打印数字不需要引号
print(42)
print(3.14)

# 可以一次打印多个内容，用逗号分隔
print("人类染色体数量:", 23, "对")


# ============================================================
# 第二部分：变量 —— 贴了标签的试管
# ============================================================

# 变量就是给数据取一个名字，方便之后使用
# 就像在试管上贴标签：标签 = 试管里的内容

# --- 字符串变量（文字，需要引号） ---
gene_name = "TP53"                      # 抑癌基因名称
organism = "Homo sapiens"               # 物种名

# --- 整数变量（整数，不需要引号） ---
chromosome_number = 17                  # TP53 位于第17号染色体
exon_count = 11                         # TP53 有11个外显子

# --- 浮点数变量（小数） ---
gc_content = 0.613                      # TP53 基因的 GC 含量

# 使用变量：直接用标签名就行
print("基因名称:", gene_name)
print("所在染色体:", chromosome_number)
print("GC 含量:", gc_content)


# ============================================================
# 第三部分：简单计算 —— Python 当计算器
# ============================================================

# Python 可以直接做数学运算，比计算器还方便

# --- 基本运算 ---
dna_length = 20000                      # 假设一段 DNA 长 20000 bp
exon_total = 3500                       # 外显子总长度 3500 bp
intron_total = dna_length - exon_total  # 内含子总长度 = 总长 - 外显子

print("DNA 总长度:", dna_length, "bp")
print("外显子总长:", exon_total, "bp")
print("内含子总长:", intron_total, "bp")

# --- 计算 GC 含量 ---
# 假设一条短序列：ATGCGCTA
g_count = 2                             # G 的数量
c_count = 2                             # C 的数量
total_bases = 8                         # 碱基总数

gc_ratio = (g_count + c_count) / total_bases  # GC 含量 = (G + C) / 总数
print("序列 ATGCGCTA 的 GC 含量:", gc_ratio)

# --- 更多运算符 ---
print("乘法: 3 × 4 =", 3 * 4)          # 乘法用 *
print("除法: 10 ÷ 3 =", 10 / 3)        # 除法用 /
print("整除: 10 ÷ 3 =", 10 // 3)       # 整除（只取整数部分）用 //
print("取余: 10 % 3 =", 10 % 3)        # 取余数用 %
print("幂运算: 2³ =", 2 ** 3)           # 幂运算用 **


# ============================================================
# 第四部分：注释 —— 给代码写备注
# ============================================================

# 以 # 开头的内容就是注释，Python 会忽略它
# 注释是写给人看的，用来解释代码的用途

# 好的注释说明"为什么"，而不仅是"是什么"

# ❌ 不太好的注释：
x = 0.05  # 把 x 设为 0.05

# ✅ 好的注释：
significance_level = 0.05  # 统计检验的显著性水平（p < 0.05 表示显著）


# ============================================================
# 小结
# ============================================================

print()
print("=" * 40)
