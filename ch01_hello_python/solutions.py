# ============================================================
# 练习3：GC 含量计算器
# ============================================================
#
# 【任务】
# 已知一条 DNA 短序列：ATGGCGCTAG
#   1. 手动数出其中 A、T、G、C 各有多少个
#   2. 把它们分别存到变量 a_count、t_count、g_count、c_count 中
#   3. 计算碱基总数（存到变量 total 中）
#   4. 计算 GC 含量：gc = (g_count + c_count) / total
#   5. 用 print() 输出 GC 含量
#
# 【示例输出】
# 序列: ATGGCGCTAG
# A: 2, T: 2, G: 3, C: 2
# (提示：还有一个碱基没数，请仔细检查！)
# 碱基总数: 10
# GC 含量: 0.5
#
# 【提示】
# - 序列是 A-T-G-G-C-G-C-T-A-G，一个一个数
# - 除法用 / 符号
# - 别忘了给代码加上注释，说明每一步在做什么
#
# --- 参考答案 ---
# 手动统计每种碱基出现的次数
a_count = 2
t_count = 2
g_count = 4
c_count = 2

# 计算总碱基数和 GC 含量
total = a_count + t_count + g_count + c_count
gc = (g_count + c_count) / total

# 输出结果
print("序列: ATGGCGCTAG")
print(f"A: {a_count}, T: {t_count}, G: {g_count}, C: {c_count}")
print("碱基总数:", total)
print("GC 含量:", gc)
